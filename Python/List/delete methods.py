mylist=[1,2,5,6,7,9,3,1]
print(mylist)
mylist.pop()           # Pop- remove last element
print(mylist)
mylist.pop(2)          # Pop- remove 2nd index element , attribute: position
print(mylist)
mylist.remove(9)       # Remove- removes the given value from the list , attribute: value
print(mylist)
del mylist[0]          # del- A python method to delete item , attribue: index
print(mylist)
mylist.clear()         # Clear- Remove all elements from a list
print(mylist)

