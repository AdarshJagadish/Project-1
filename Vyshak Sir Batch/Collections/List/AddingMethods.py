l=[]
l.append(10)                        # To add a single element to the end of a list
print(l)
l.append([10,12])                        # To add a list as an element to the end of a list (Nested List)
print(l)
l.extend([20,30,40])                        # To add a multiple element to the end of a list
print(l)
l.insert(2,10)                        # To insert a single element to a specific index position in a list, (index,value)
print(l)
